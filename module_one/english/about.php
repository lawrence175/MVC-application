 <bold>Who we are:</bold>
 <h3>
<p>True Mercy Africa is a charity organisation with a merciful heart for vulnerable children and youth for a long time, 
</p>
<p>The help of a group of incredible youth and God fearing people,We operate in Uganda and reaches out to
abandon, rejected, orphaned children and Elderly caregivers.
We envision to giving hope and care to all vulnerable children, youth and their
caregivers.</p>
<p> We look at the mental, social and health development of the children and youths. 
We provide them with quality formal and informal education, train them good health practices, nurture them in talent development and train them in livelihood skills leading them to being self-sustained and job creator Christians.
TMA is a broad ministry that plans to reach all areas of Uganda and later to the rest of
Africa. We plan to operate in five major areas of Uganda, TMA-Central Uganda, TMA-
Eastern Uganda, TMA-Western Uganda, TMA-Northern Uganda, and TMA-Southern
Uganda.</h3></p>
